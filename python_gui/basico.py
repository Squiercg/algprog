###RadioButtons

cores=['Blue','Gold','Red']

def chamaRadio():
	radSel=radVar.get()
	if(radSel==0):
		janela.configure(background=cores[0])
	elif(radSel==1):
		janela.configure(background=cores[1])
	elif(radSel==2):
		janela.configure(background=cores[2])

radVar=tk.IntVar()
radVar.set(-1)

for cor in range(3):
	radio_button = 'rad'+str(cor)
	radio_button = tk.Radiobutton(janela,text=cores[cor],variable=radVar,value=cor,command=chamaRadio)
	radio_button.grid(column=cor,row=5,sticky=tk.W)

